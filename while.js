//syntax and example of while loop
//while is an entry controlled loop
let i=0;
while(i<10)
{
    console.log("hi there,.....");
    i++;
}

//do while syntax and example
//do while is an exit control loop

let j=0;
do{
    console.log("yo hoo");
    j++;
}while(j>10);//the condition is checked after it executes ones